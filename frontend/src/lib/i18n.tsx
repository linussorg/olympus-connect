const LOCALE_KEY = "olympus-locale";

function getStoredLocale(): Locale {
  const stored = localStorage.getItem(LOCALE_KEY);
  if (stored === "en" || stored === "de") return stored;
  return "de";
}

export function getLocale(): Locale {
  return getStoredLocale();
}

